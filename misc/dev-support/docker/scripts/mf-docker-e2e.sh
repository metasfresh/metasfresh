#!/bin/bash
#
# mf-docker-e2e.sh - Manage E2E test stack using CI-built Docker images
#
# Usage: mf-docker-e2e.sh [start|stop|status|logs] [options]
#
# Commands:
#   start       Start the full metasfresh stack
#   stop        Stop all containers
#   status      Show container status and health
#   logs        Tail logs (optionally specify service name)
#
# Options:
#   --no-webui      Don't start webui (for local frontend development)
#   --no-mobile     Don't start mobile ui
#   --dev-mode      Mount source code for hot reload (experimental)
#

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
METASFRESH_ROOT="$(cd "$SCRIPT_DIR/../../../.." && pwd)"
VERSION_FILE="$SCRIPT_DIR/.mf-docker-version"
# Use frontendtest compose which has FRONTEND_TESTING=true and Jasper config
COMPOSE_DIR="$METASFRESH_ROOT/docker-builds/frontendtest"
PROJECT_NAME="mfstack"

# User ID/GID for docker run commands (ensures files are owned by current user)
DOCKER_USER="$(id -u):$(id -g)"

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m' # No Color

# Helper functions
info() { echo -e "${BLUE}→${NC} $1"; }
success() { echo -e "${GREEN}✓${NC} $1"; }
warn() { echo -e "${YELLOW}!${NC} $1"; }
error() { echo -e "${RED}✗${NC} $1" >&2; }

# Check prerequisites
check_prerequisites() {
    # Check Docker
    if ! command -v docker &> /dev/null; then
        error "Docker is not available."
        echo "  Make sure Docker Desktop is running with WSL2 integration enabled."
        exit 1
    fi

    # Test Docker connection
    if ! docker info &> /dev/null; then
        error "Cannot connect to Docker daemon."
        echo "  Is Docker Desktop running?"
        exit 1
    fi

    # Check docker compose
    if ! docker compose version &> /dev/null; then
        error "docker compose is not available."
        exit 1
    fi
}

# Get saved image tag
get_image_tag() {
    if [ ! -f "$VERSION_FILE" ]; then
        error "No image tag found. Run 'mf-docker.sh pull <branch>' first."
        exit 1
    fi

    cat "$VERSION_FILE" | tr -d '[:space:]'
}

# Generate .env file with the correct tag
generate_env_file() {
    local tag="$1"

    cat > "$COMPOSE_DIR/.env" << EOF
# Generated by mf-docker-e2e.sh
# Tag: $tag
# Generated: $(date -Iseconds)

mfregistry=metasfresh
mfversion=$tag
dbqualifier=preloaded

# For compose-dev.yml volume mounts (used in dev-mode)
METASFRESH_ROOT=$METASFRESH_ROOT
EOF

    success "Generated $COMPOSE_DIR/.env with tag: $tag"
}

# Build compose command with proper services
build_compose_cmd() {
    local cmd="docker compose -p $PROJECT_NAME -f $COMPOSE_DIR/compose.yml"

    # Add dev overlay if requested
    if [ "$DEV_MODE" == "true" ] && [ -f "$COMPOSE_DIR/compose-dev.yml" ]; then
        cmd="$cmd -f $COMPOSE_DIR/compose-dev.yml"
    fi

    echo "$cmd"
}

# Get list of services to start (matches frontendtest/compose.yml)
get_services() {
    # Core services for E2E testing (no playwright - we run tests locally)
    local services="db rabbitmq search webapi app"

    if [ "$NO_WEBUI" != "true" ]; then
        services="$services webui"
    fi

    echo "$services"
}

# Extract artifacts from CI images for dev-mode
# This allows zero-build startup - no local Maven/yarn build required
extract_artifacts_if_needed() {
    local tag="$1"
    local artifacts_dir="$METASFRESH_ROOT/.dev-artifacts"
    local registry="metasfresh"

    echo ""
    info "Preparing dev-mode artifacts..."

    # Create artifacts directories
    mkdir -p "$artifacts_dir"/{webapi,app,webui,reports}

    # Extract webapi JAR if not exists
    if [ ! -f "$artifacts_dir/webapi/metasfresh-webui-api.jar" ]; then
        info "Extracting webapi JAR from $registry/metas-api:$tag..."
        docker run --rm --user "$DOCKER_USER" -v "$artifacts_dir/webapi:/out" \
            "$registry/metas-api:$tag" \
            cp /opt/metasfresh-webui-api/metasfresh-webui-api.jar /out/
        success "Extracted webapi JAR"
    else
        info "Using existing webapi JAR (delete .dev-artifacts/ to refresh)"
    fi

    # Extract app JAR if not exists
    if [ ! -f "$artifacts_dir/app/metasfresh_server.jar" ]; then
        info "Extracting app JAR from $registry/metas-app:$tag..."
        docker run --rm --user "$DOCKER_USER" -v "$artifacts_dir/app:/out" \
            "$registry/metas-app:$tag" \
            cp /opt/metasfresh/metasfresh_server.jar /out/
        success "Extracted app JAR"
    else
        info "Using existing app JAR (delete .dev-artifacts/ to refresh)"
    fi

    # Extract reports if not exists (JRXML files for Jasper reports)
    if [ ! -d "$artifacts_dir/reports/jasper" ] || [ -z "$(ls -A "$artifacts_dir/reports" 2>/dev/null)" ]; then
        info "Extracting reports from $registry/metas-app:$tag..."
        docker run --rm --user "$DOCKER_USER" -v "$artifacts_dir/reports:/out" \
            "$registry/metas-app:$tag" \
            sh -c "cp -r /opt/metasfresh/reports/* /out/ 2>/dev/null || echo 'No reports directory'"
        success "Extracted reports"
    else
        info "Using existing reports (delete .dev-artifacts/ to refresh)"
    fi

    # Extract frontend assets if not exists
    if [ ! -f "$artifacts_dir/webui/index.html" ]; then
        info "Extracting frontend assets from $registry/metas-frontend:$tag..."
        docker run --rm --user "$DOCKER_USER" -v "$artifacts_dir/webui:/out" \
            "$registry/metas-frontend:$tag" \
            sh -c "cp -r /usr/share/nginx/html/* /out/"
        success "Extracted frontend assets"
    else
        info "Using existing frontend assets (delete .dev-artifacts/ to refresh)"
    fi

    # Always copy web-config.js to webui assets (ensures proper config.js)
    # This is critical because the extracted config.js from CI may have incorrect URLs
    local config_src="$COMPOSE_DIR/web-config.js"
    if [ -f "$config_src" ]; then
        cp "$config_src" "$artifacts_dir/webui/config.js"
        success "Copied config.js to webui artifacts"
    fi

    echo ""
    success "Dev-mode artifacts ready in $artifacts_dir"
    echo ""
}

# Start the stack
cmd_start() {
    local tag=$(get_image_tag)
    local services=$(get_services)

    echo ""
    echo "╔════════════════════════════════════════════════════════════╗"
    echo "║           Starting metasfresh E2E Stack                   ║"
    echo "╚════════════════════════════════════════════════════════════╝"
    echo ""

    info "Image tag: $tag"
    info "Services: $services"
    if [ "$DEV_MODE" == "true" ]; then
        info "Dev mode: ENABLED (hot-reload support)"
    fi
    echo ""

    # Generate .env file
    generate_env_file "$tag"

    # Extract artifacts from CI images for dev-mode (zero-build startup)
    if [ "$DEV_MODE" == "true" ]; then
        extract_artifacts_if_needed "$tag"
    fi

    # Start services
    info "Starting containers..."
    local compose_cmd=$(build_compose_cmd)

    $compose_cmd up -d $services

    echo ""
    info "Waiting for services to become healthy..."
    echo ""

    # Wait for health checks (with timeout)
    local timeout=180
    local elapsed=0
    local interval=5

    while [ $elapsed -lt $timeout ]; do
        local unhealthy=$($compose_cmd ps --format json 2>/dev/null | jq -r 'select(.Health == "unhealthy" or .Health == "starting") | .Name' | wc -l)

        if [ "$unhealthy" -eq 0 ]; then
            break
        fi

        echo -ne "\r  Waiting... ${elapsed}s ($(echo "$unhealthy") services still starting)    "
        sleep $interval
        elapsed=$((elapsed + interval))
    done

    echo ""
    echo ""

    # Show final status
    cmd_status

    echo ""
    echo "╔════════════════════════════════════════════════════════════╗"
    echo "║  Stack is ready! Access points:                           ║"
    echo "║                                                            ║"
    if [ "$NO_WEBUI" != "true" ]; then
    echo "║  • Frontend:  http://localhost:3000                       ║"
    else
    echo "║  • Frontend:  (not started - use local yarn start)        ║"
    fi
    echo "║  • API:       http://localhost:8080                       ║"
    echo "║  • App:       http://localhost:8282                       ║"
    echo "║  • Database:  localhost:14432 (MCP: postgres-e2e)        ║"
    if [ "$DEV_MODE" == "true" ]; then
    echo "║                                                            ║"
    echo "║  Dev mode: Use 'mf-docker.sh build <component>' to update ║"
    fi
    echo "╚════════════════════════════════════════════════════════════╝"
    echo ""
}

# Stop the stack
cmd_stop() {
    echo ""
    info "Stopping metasfresh stack..."

    local compose_cmd=$(build_compose_cmd)
    $compose_cmd down

    success "Stack stopped"
    echo ""
}

# Show status
cmd_status() {
    local compose_cmd=$(build_compose_cmd)

    echo ""
    echo "╔════════════════════════════════════════════════════════════╗"
    echo "║           metasfresh E2E Stack Status                     ║"
    echo "╚════════════════════════════════════════════════════════════╝"
    echo ""

    if [ -f "$VERSION_FILE" ]; then
        info "Image tag: $(cat "$VERSION_FILE")"
    else
        warn "No image tag set (run 'mf-docker.sh pull' first)"
    fi
    echo ""

    # Show container status with health
    echo "  Service         Status        Health"
    echo "  ─────────────────────────────────────────"

    $compose_cmd ps --format "table {{.Name}}\t{{.State}}\t{{.Health}}" 2>/dev/null | tail -n +2 | while read line; do
        # Color code based on health
        if echo "$line" | grep -q "healthy"; then
            echo -e "  ${GREEN}$line${NC}"
        elif echo "$line" | grep -q "unhealthy\|starting"; then
            echo -e "  ${YELLOW}$line${NC}"
        elif echo "$line" | grep -q "running"; then
            echo -e "  ${CYAN}$line${NC}"
        else
            echo -e "  ${RED}$line${NC}"
        fi
    done

    echo ""
}

# Show logs
cmd_logs() {
    local service="$1"
    local compose_cmd=$(build_compose_cmd)

    if [ -n "$service" ]; then
        info "Tailing logs for: $service (Ctrl+C to stop)"
        $compose_cmd logs -f "$service"
    else
        info "Tailing all logs (Ctrl+C to stop)"
        $compose_cmd logs -f
    fi
}

# Parse options
parse_options() {
    while [[ $# -gt 0 ]]; do
        case $1 in
            --no-webui)
                NO_WEBUI=true
                shift
                ;;
            --no-mobile)
                NO_MOBILE=true
                shift
                ;;
            --dev-mode)
                DEV_MODE=true
                shift
                ;;
            *)
                # Not an option, might be a command or service name
                REMAINING_ARGS+=("$1")
                shift
                ;;
        esac
    done
}

# Show help
show_help() {
    echo "Usage: mf-docker-e2e.sh [command] [options]"
    echo ""
    echo "Commands:"
    echo "  start       Start the full metasfresh stack"
    echo "  stop        Stop all containers"
    echo "  status      Show container status and health"
    echo "  logs [svc]  Tail logs (optionally specify service)"
    echo ""
    echo "Options:"
    echo "  --no-webui    Don't start webui (for local frontend dev)"
    echo "  --no-mobile   Don't start mobile ui"
    echo "  --dev-mode    Mount source for hot reload (experimental)"
    echo ""
    echo "Examples:"
    echo "  mf-docker-e2e.sh start"
    echo "  mf-docker-e2e.sh start --no-webui"
    echo "  mf-docker-e2e.sh logs webapi"
    echo "  mf-docker-e2e.sh stop"
}

# Main
main() {
    local args=("$@")
    REMAINING_ARGS=()

    parse_options "${args[@]}"

    local command="${REMAINING_ARGS[0]}"

    check_prerequisites

    case "$command" in
        start)
            cmd_start
            ;;
        stop)
            cmd_stop
            ;;
        status)
            cmd_status
            ;;
        logs)
            cmd_logs "${REMAINING_ARGS[1]}"
            ;;
        help|--help|-h)
            show_help
            ;;
        *)
            if [ -z "$command" ]; then
                show_help
            else
                error "Unknown command: $command"
                echo ""
                show_help
                exit 1
            fi
            ;;
    esac
}

main "$@"
