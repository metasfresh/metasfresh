#!/bin/bash
#
# mf-docker-e2e.sh - Manage E2E test stack using CI-built Docker images
#
# Usage: mf-docker-e2e.sh [start|stop|status|logs] [options]
#
# Commands:
#   start       Start the full metasfresh stack
#   stop        Stop all containers
#   status      Show container status and health
#   logs        Tail logs (optionally specify service name)
#
# Options:
#   --no-webui      Don't start webui (for local frontend development)
#   --no-mobile     Don't start mobile ui
#   --dev-mode      Mount source code for hot reload (experimental)
#   --repo <name>   Use custom repo (reads from .mf-docker-version if not specified)
#   --repo-root <path>  Path to custom repo checkout
#

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
METASFRESH_ROOT="$(cd "$SCRIPT_DIR/../../../.." && pwd)"
VERSION_FILE="$SCRIPT_DIR/.mf-docker-version"
# Default to frontendtest compose which has FRONTEND_TESTING=true and Jasper config
DEFAULT_COMPOSE_DIR="$METASFRESH_ROOT/docker-builds/frontendtest"
PROJECT_NAME="mfstack"

# User ID/GID for docker run commands (ensures files are owned by current user)
DOCKER_USER="$(id -u):$(id -g)"

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m' # No Color

# Helper functions
info() { echo -e "${BLUE}→${NC} $1"; }
success() { echo -e "${GREEN}✓${NC} $1"; }
warn() { echo -e "${YELLOW}!${NC} $1"; }
error() { echo -e "${RED}✗${NC} $1" >&2; }

# Check prerequisites
check_prerequisites() {
    # Check Docker
    if ! command -v docker &> /dev/null; then
        error "Docker is not available."
        echo "  Make sure Docker Desktop is running with WSL2 integration enabled."
        exit 1
    fi

    # Test Docker connection
    if ! docker info &> /dev/null; then
        error "Cannot connect to Docker daemon."
        echo "  Is Docker Desktop running?"
        exit 1
    fi

    # Check docker compose
    if ! docker compose version &> /dev/null; then
        error "docker compose is not available."
        exit 1
    fi
}

# Login to GitHub Container Registry
ghcr_login() {
    info "Logging into GitHub Container Registry..."
    local gh_user
    gh_user=$(gh api user -q .login 2>/dev/null) || gh_user="token"

    if echo "$(gh auth token)" | docker login ghcr.io -u "$gh_user" --password-stdin 2>/dev/null; then
        success "Logged into ghcr.io"
    else
        warn "Failed to login to GHCR - images may fail to pull"
    fi
}

# Load configuration from version file
load_version_config() {
    if [ ! -f "$VERSION_FILE" ]; then
        error "No configuration found. Run 'mf-docker.sh pull <branch>' first."
        exit 1
    fi

    # Check if it's the new format (key=value) or old format (just tag)
    if grep -q "^TAG=" "$VERSION_FILE" 2>/dev/null; then
        # New format - source it
        source "$VERSION_FILE"
        CONFIG_TAG="$TAG"
        CONFIG_REPO="$REPO"
        CONFIG_REPO_ROOT="$REPO_ROOT"
        CONFIG_REGISTRY="$REGISTRY"
    else
        # Old format - just the tag
        CONFIG_TAG=$(cat "$VERSION_FILE" | tr -d '[:space:]')
        CONFIG_REPO=""
        CONFIG_REPO_ROOT="$METASFRESH_ROOT"
        CONFIG_REGISTRY="metasfresh"
    fi
}

# Determine compose directory based on configuration
get_compose_dir() {
    local repo="$1"
    local repo_root="$2"

    # If custom repo is specified and has a compose.yml, use it
    if [ -n "$repo" ] && [ -n "$repo_root" ]; then
        local custom_compose="$repo_root/docker-builds/compose"
        if [ -f "$custom_compose/compose.yml" ]; then
            echo "$custom_compose"
            return
        else
            warn "Custom repo compose.yml not found at $custom_compose"
            warn "Falling back to default compose"
        fi
    fi

    echo "$DEFAULT_COMPOSE_DIR"
}

# Generate .env file for standard metasfresh compose
generate_standard_env_file() {
    local compose_dir="$1"
    local tag="$2"

    cat > "$compose_dir/.env" << EOF
# Generated by mf-docker-e2e.sh
# Tag: $tag
# Generated: $(date -Iseconds)

mfregistry=metasfresh
mfversion=$tag
dbqualifier=preloaded

# For compose-dev.yml volume mounts (used in dev-mode)
METASFRESH_ROOT=$METASFRESH_ROOT
EOF

    success "Generated $compose_dir/.env with tag: $tag"
}

# Generate .env file for custom repo compose (e.g., ms205)
# NOTE: The compose.yml already appends -compat where needed, so we don't add it here
generate_custom_env_file() {
    local compose_dir="$1"
    local tag="$2"
    local repo="$3"
    local registry="$4"

    cat > "$compose_dir/.env" << EOF
# Generated by mf-docker-e2e.sh
# Custom repo: $repo
# Tag: $tag
# Generated: $(date -Iseconds)

mfregistry=$registry
mfversion=$tag
EOF

    success "Generated $compose_dir/.env for $repo with tag: $tag"
}

# Build compose command with proper services
build_compose_cmd() {
    local compose_dir="$1"
    local cmd="docker compose -p $PROJECT_NAME -f $compose_dir/compose.yml"

    # Add dev overlay if requested and exists
    if [ "$DEV_MODE" == "true" ] && [ -f "$compose_dir/compose-dev.yml" ]; then
        cmd="$cmd -f $compose_dir/compose-dev.yml"
    fi

    echo "$cmd"
}

# Get list of services to start
get_services() {
    local compose_dir="$1"
    local is_custom="$2"

    # Core services for E2E testing
    local services="db rabbitmq search webapi app"

    if [ "$NO_WEBUI" != "true" ]; then
        services="$services webui"
    fi

    # Custom repos may have additional services (external, edi)
    if [ "$is_custom" == "true" ]; then
        # Check if compose has these services
        if grep -q "external:" "$compose_dir/compose.yml" 2>/dev/null; then
            services="$services external"
        fi
        if grep -q "edi:" "$compose_dir/compose.yml" 2>/dev/null; then
            services="$services edi"
        fi
    fi

    echo "$services"
}

# Extract artifacts from CI images for dev-mode
# This allows zero-build startup - no local Maven/yarn build required
extract_artifacts_if_needed() {
    local tag="$1"
    local registry="$2"
    local is_custom="$3"
    local artifacts_dir="$METASFRESH_ROOT/.dev-artifacts"

    echo ""
    info "Preparing dev-mode artifacts..."

    # Adjust image names based on registry type
    local api_image app_image frontend_image
    if [ "$is_custom" == "true" ]; then
        api_image="$registry/metasfresh-webapi:$tag-compat"
        app_image="$registry/metasfresh-app:$tag-compat"
        frontend_image="$registry/metasfresh-webui:$tag-compat"
    else
        api_image="$registry/metas-api:$tag"
        app_image="$registry/metas-app:$tag"
        frontend_image="$registry/metas-frontend:$tag"
    fi

    # Create artifacts directories
    mkdir -p "$artifacts_dir"/{webapi,app,webui,reports}

    # Extract webapi JAR if not exists
    if [ ! -f "$artifacts_dir/webapi/metasfresh-webui-api.jar" ]; then
        info "Extracting webapi JAR from $api_image..."
        docker run --rm --user "$DOCKER_USER" -v "$artifacts_dir/webapi:/out" \
            "$api_image" \
            cp /opt/metasfresh-webui-api/metasfresh-webui-api.jar /out/
        success "Extracted webapi JAR"
    else
        info "Using existing webapi JAR (delete .dev-artifacts/ to refresh)"
    fi

    # Extract app JAR if not exists
    if [ ! -f "$artifacts_dir/app/metasfresh_server.jar" ]; then
        info "Extracting app JAR from $app_image..."
        docker run --rm --user "$DOCKER_USER" -v "$artifacts_dir/app:/out" \
            "$app_image" \
            cp /opt/metasfresh/metasfresh_server.jar /out/
        success "Extracted app JAR"
    else
        info "Using existing app JAR (delete .dev-artifacts/ to refresh)"
    fi

    # Extract reports if not exists (JRXML files for Jasper reports)
    if [ ! -d "$artifacts_dir/reports/jasper" ] || [ -z "$(ls -A "$artifacts_dir/reports" 2>/dev/null)" ]; then
        info "Extracting reports from $app_image..."
        docker run --rm --user "$DOCKER_USER" -v "$artifacts_dir/reports:/out" \
            "$app_image" \
            sh -c "cp -r /opt/metasfresh/reports/* /out/ 2>/dev/null || echo 'No reports directory'"
        success "Extracted reports"
    else
        info "Using existing reports (delete .dev-artifacts/ to refresh)"
    fi

    # Extract frontend assets if not exists
    if [ ! -f "$artifacts_dir/webui/index.html" ]; then
        info "Extracting frontend assets from $frontend_image..."
        docker run --rm --user "$DOCKER_USER" -v "$artifacts_dir/webui:/out" \
            "$frontend_image" \
            sh -c "cp -r /usr/share/nginx/html/* /out/"
        success "Extracted frontend assets"
    else
        info "Using existing frontend assets (delete .dev-artifacts/ to refresh)"
    fi

    # Always copy web-config.js to webui assets (ensures proper config.js)
    # This is critical because the extracted config.js from CI may have incorrect URLs
    local config_src="$COMPOSE_DIR/web-config.js"
    if [ -f "$config_src" ]; then
        cp "$config_src" "$artifacts_dir/webui/config.js"
        success "Copied config.js to webui artifacts"
    fi

    echo ""
    success "Dev-mode artifacts ready in $artifacts_dir"
    echo ""
}

# Start the stack
cmd_start() {
    # Load configuration
    load_version_config

    # Override with command-line args if provided
    [ -n "$REPO_ARG" ] && CONFIG_REPO="$REPO_ARG"
    [ -n "$REPO_ROOT_ARG" ] && CONFIG_REPO_ROOT="$REPO_ROOT_ARG"

    local is_custom="false"
    [ -n "$CONFIG_REPO" ] && is_custom="true"

    # Determine compose directory
    COMPOSE_DIR=$(get_compose_dir "$CONFIG_REPO" "$CONFIG_REPO_ROOT")

    local services=$(get_services "$COMPOSE_DIR" "$is_custom")

    echo ""
    echo "╔════════════════════════════════════════════════════════════╗"
    echo "║           Starting metasfresh E2E Stack                   ║"
    echo "╚════════════════════════════════════════════════════════════╝"
    echo ""

    info "Image tag: $CONFIG_TAG"
    if [ "$is_custom" == "true" ]; then
        info "Custom repo: $CONFIG_REPO"
        info "Registry: $CONFIG_REGISTRY"
    fi
    info "Compose: $COMPOSE_DIR"
    info "Services: $services"
    if [ "$DEV_MODE" == "true" ]; then
        info "Dev mode: ENABLED (hot-reload support)"
    fi
    echo ""

    # Login to GHCR if using custom repo
    if [ "$is_custom" == "true" ]; then
        ghcr_login
        echo ""
    fi

    # Generate .env file
    if [ "$is_custom" == "true" ]; then
        generate_custom_env_file "$COMPOSE_DIR" "$CONFIG_TAG" "$CONFIG_REPO" "$CONFIG_REGISTRY"
    else
        generate_standard_env_file "$COMPOSE_DIR" "$CONFIG_TAG"
    fi

    # Extract artifacts from CI images for dev-mode (zero-build startup)
    if [ "$DEV_MODE" == "true" ]; then
        extract_artifacts_if_needed "$CONFIG_TAG" "$CONFIG_REGISTRY" "$is_custom"
    fi

    # Start services
    info "Starting containers..."
    local compose_cmd=$(build_compose_cmd "$COMPOSE_DIR")

    $compose_cmd up -d $services

    echo ""
    info "Waiting for services to become healthy..."
    echo ""

    # Wait for health checks (with timeout)
    local timeout=180
    local elapsed=0
    local interval=5

    while [ $elapsed -lt $timeout ]; do
        local unhealthy=$($compose_cmd ps --format json 2>/dev/null | jq -r 'select(.Health == "unhealthy" or .Health == "starting") | .Name' | wc -l)

        if [ "$unhealthy" -eq 0 ]; then
            break
        fi

        echo -ne "\r  Waiting... ${elapsed}s ($(echo "$unhealthy") services still starting)    "
        sleep $interval
        elapsed=$((elapsed + interval))
    done

    echo ""
    echo ""

    # Show final status
    cmd_status

    # Determine ports from compose file
    local db_port="15432"
    local webui_port="80"
    if [ "$is_custom" == "true" ]; then
        # Custom repos use standard ports
        db_port="15432"
        webui_port="80"
    else
        # frontendtest uses different ports
        db_port="14432"
        webui_port="3000"
    fi

    echo ""
    echo "╔════════════════════════════════════════════════════════════╗"
    echo "║  Stack is ready! Access points:                           ║"
    echo "║                                                            ║"
    if [ "$NO_WEBUI" != "true" ]; then
    echo "║  • Frontend:  http://localhost:$webui_port                       ║"
    else
    echo "║  • Frontend:  (not started - use local yarn start)        ║"
    fi
    echo "║  • API:       http://localhost:8080                       ║"
    echo "║  • App:       http://localhost:8282                       ║"
    echo "║  • Database:  localhost:$db_port                          ║"
    if [ "$DEV_MODE" == "true" ]; then
    echo "║                                                            ║"
    echo "║  Dev mode: Use 'mf-docker.sh build <component>' to update ║"
    fi
    echo "╚════════════════════════════════════════════════════════════╝"
    echo ""
}

# Stop the stack
cmd_stop() {
    # Load configuration to get compose dir
    load_version_config

    [ -n "$REPO_ARG" ] && CONFIG_REPO="$REPO_ARG"
    [ -n "$REPO_ROOT_ARG" ] && CONFIG_REPO_ROOT="$REPO_ROOT_ARG"

    COMPOSE_DIR=$(get_compose_dir "$CONFIG_REPO" "$CONFIG_REPO_ROOT")

    echo ""
    info "Stopping metasfresh stack..."

    local compose_cmd=$(build_compose_cmd "$COMPOSE_DIR")
    $compose_cmd down

    success "Stack stopped"
    echo ""
}

# Show status
cmd_status() {
    # Load configuration
    load_version_config

    [ -n "$REPO_ARG" ] && CONFIG_REPO="$REPO_ARG"
    [ -n "$REPO_ROOT_ARG" ] && CONFIG_REPO_ROOT="$REPO_ROOT_ARG"

    COMPOSE_DIR=$(get_compose_dir "$CONFIG_REPO" "$CONFIG_REPO_ROOT")
    local compose_cmd=$(build_compose_cmd "$COMPOSE_DIR")

    echo ""
    echo "╔════════════════════════════════════════════════════════════╗"
    echo "║           metasfresh E2E Stack Status                     ║"
    echo "╚════════════════════════════════════════════════════════════╝"
    echo ""

    if [ -f "$VERSION_FILE" ]; then
        info "Image tag: $CONFIG_TAG"
        if [ -n "$CONFIG_REPO" ]; then
            info "Custom repo: $CONFIG_REPO"
        fi
    else
        warn "No image tag set (run 'mf-docker.sh pull' first)"
    fi
    echo ""

    # Show container status with health
    echo "  Service         Status        Health"
    echo "  ─────────────────────────────────────────"

    $compose_cmd ps --format "table {{.Name}}\t{{.State}}\t{{.Health}}" 2>/dev/null | tail -n +2 | while read line; do
        # Color code based on health
        if echo "$line" | grep -q "healthy"; then
            echo -e "  ${GREEN}$line${NC}"
        elif echo "$line" | grep -q "unhealthy\|starting"; then
            echo -e "  ${YELLOW}$line${NC}"
        elif echo "$line" | grep -q "running"; then
            echo -e "  ${CYAN}$line${NC}"
        else
            echo -e "  ${RED}$line${NC}"
        fi
    done

    echo ""
}

# Show logs
cmd_logs() {
    local service="$1"

    # Load configuration
    load_version_config

    [ -n "$REPO_ARG" ] && CONFIG_REPO="$REPO_ARG"
    [ -n "$REPO_ROOT_ARG" ] && CONFIG_REPO_ROOT="$REPO_ROOT_ARG"

    COMPOSE_DIR=$(get_compose_dir "$CONFIG_REPO" "$CONFIG_REPO_ROOT")
    local compose_cmd=$(build_compose_cmd "$COMPOSE_DIR")

    if [ -n "$service" ]; then
        info "Tailing logs for: $service (Ctrl+C to stop)"
        $compose_cmd logs -f "$service"
    else
        info "Tailing all logs (Ctrl+C to stop)"
        $compose_cmd logs -f
    fi
}

# Parse options
parse_options() {
    while [[ $# -gt 0 ]]; do
        case $1 in
            --no-webui)
                NO_WEBUI=true
                shift
                ;;
            --no-mobile)
                NO_MOBILE=true
                shift
                ;;
            --dev-mode)
                DEV_MODE=true
                shift
                ;;
            --repo)
                REPO_ARG="$2"
                shift 2
                ;;
            --repo-root)
                REPO_ROOT_ARG="$2"
                shift 2
                ;;
            *)
                # Not an option, might be a command or service name
                REMAINING_ARGS+=("$1")
                shift
                ;;
        esac
    done
}

# Show help
show_help() {
    echo "Usage: mf-docker-e2e.sh [command] [options]"
    echo ""
    echo "Commands:"
    echo "  start       Start the full metasfresh stack"
    echo "  stop        Stop all containers"
    echo "  status      Show container status and health"
    echo "  logs [svc]  Tail logs (optionally specify service)"
    echo ""
    echo "Options:"
    echo "  --no-webui        Don't start webui (for local frontend dev)"
    echo "  --no-mobile       Don't start mobile ui"
    echo "  --dev-mode        Mount source for hot reload (experimental)"
    echo "  --repo <name>     Use custom repo (overrides .mf-docker-version)"
    echo "  --repo-root <path> Path to custom repo checkout"
    echo ""
    echo "Examples:"
    echo "  # Start with standard metasfresh images"
    echo "  mf-docker-e2e.sh start"
    echo ""
    echo "  # Start with custom repo (e.g., ms205)"
    echo "  mf-docker-e2e.sh start --repo ms205 --repo-root ../ms205"
    echo ""
    echo "  # Other commands"
    echo "  mf-docker-e2e.sh start --no-webui"
    echo "  mf-docker-e2e.sh logs webapi"
    echo "  mf-docker-e2e.sh stop"
}

# Main
main() {
    local args=("$@")
    REMAINING_ARGS=()

    parse_options "${args[@]}"

    local command="${REMAINING_ARGS[0]}"

    check_prerequisites

    case "$command" in
        start)
            cmd_start
            ;;
        stop)
            cmd_stop
            ;;
        status)
            cmd_status
            ;;
        logs)
            cmd_logs "${REMAINING_ARGS[1]}"
            ;;
        help|--help|-h)
            show_help
            ;;
        *)
            if [ -z "$command" ]; then
                show_help
            else
                error "Unknown command: $command"
                echo ""
                show_help
                exit 1
            fi
            ;;
    esac
}

main "$@"
